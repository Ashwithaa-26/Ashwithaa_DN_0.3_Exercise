package com.mycompany.bookcustomermanagement.service;

import com.library.api.repository.BookRepository;
import com.mycompany.bookcustomermanagement.Book;
import com.mycompany.bookcustomermanagement.repository.BookRepository;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

import java.util.List;
import java.util.Optional;

@Service
public class BookService {

    @Autowired
    private BookRepository bookRepository;

    public Book createBook(Book book) {
        return bookRepository.save(book);
    }

    public List<com.library.api.entity.Book> getAllBooks() {
        return bookRepository.findAll();
    }

    public Object getBookById(Long id) {
        return bookRepository.findById(id);
    }

    public Book updateBook(Book book) {
        return bookRepository.save(book);
    }

    public void deleteBook(Long id) {
        bookRepository.deleteById(id);
    }
}
